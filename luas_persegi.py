# PROGRAM PERSEGI
    # menerima panjang dan lebar dari user kemudian menampilkan luas persegi panjang ke layar
# KAMUS
    # Variabel  
        # panjang : integer
        # lebar : integer
        # luas : integer
# ALGORITMA UTAMA

# Langkah 1 : menerima panjang dari user
print("masukan panjang : ")
#panjang = input() # tipe data : string
panjang = int(input()) # casting tipe data dari string menjadi integer

# Langkah 2 : menerima lebar dari user
print("masukan lebar : ")
lebar = int(input())

# Langkah 3 : menghitung luas
luas = panjang * lebar

# Langkah 4 : menampilkan luas ke layar
print(luas)

# CATATAN
# tipe data
    # 1. bilangan bulat (integer) : x = 1, y =17, z = 69
    # 2. bilangan real (float) : a = 3.14 , b = 0.002
    # 3. string : nama = "udin", lawan = "erik"


